$(document).ready(function() {
    var params = new window.URLSearchParams(window.location.search);
    let idToFind =params.get('id');

    let blogFinded= null;
    $.each(blogs, function(index, blog) {
     if (blog.blogId == idToFind) {
        blogFinded = blog;
       return false;
     }
   });
   if(blogFinded != null){
    $("#tittle_blog").text(blogFinded.tittle);
    let img = $("#img_blog");
    img.attr('src', blogFinded.img);
    img.attr('alt', blogFinded.tittle);
    // $("#blog-container").html(blogFinded.info).fadeIn(10000);

    $("#blog-container").animate({ opacity: 0 }, 1000, "easeOutBounce", function() {
        $(this).html(blogFinded.info).animate({ opacity: 1 }, 1000, "easeOutBounce");
      });
   }

   $("#img_blog").click(function(){
    $(this).css("position","absolute");
    $(this).animate({
        top:"+=100px"
        }, {
            duration: 2000,
            easing: "easeInOutElastic",
            complete: function() {
                $(this).animate({
                    top: "-=100px",
                }, {
                    duration: 1000, 
                    easing: "easeInOutElastic", 
                });
                $(this).css("position","");

            }
        });
   });


});